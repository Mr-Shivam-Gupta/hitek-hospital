                              <p class="text-justify">Revascularization is the process that treats the disease related to coronary arteries when they become severely blocked. This is due to a condition called atherosclerosis, where large arteries get narrowed, thereby limiting the blood supply to the vital organs of the body. This can lead to chest pain, resulting in a possible heart attack. This condition can be treated by the use of medication and also by healthy living. That said, if the arteries are blocked to a higher magnitude, the procedure of revascularization comes into the picture.<br><br>
There are two ways in which the coronary artery that is blocked can be sorted; first by implanting a stent in the blockage area, which is generally done through a procedure called Angioplasty, and second, by entirely bypassing the blocked segment of the artery through a surgical procedure commonly referred to as Bypass Surgery.<br><br>
The heart has three types of major coronary arteries, each supplying different areas of the heart muscle. In the case of a single artery blockage, angioplasty is the procedure preferred as it happens to be less invasive. When multiple arteries get blocked, a decision needs to be made whether to go ahead with angioplasty or a bypass. There can be instances where either of the two revascularizations is possible.<br><br>
Some of the many reasons why this disease can crop up is unhealthy diet, high blood pressure, smoking, resistance to insulin or diabetes, and high cholesterol. When it comes to the symptoms, initially the decreased blood flow may not cause any issue, but as and when the plaque builds, chest pain, shortness of breath, and heart attack become pretty evident.
